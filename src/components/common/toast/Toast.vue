<template>
  <div class="toast" v-show="isShow">
    <div >{{message}}</div>
  </div>
</template>

<script>
  
  export default {
    name: 'Toast',
    data() {
      return {
        message:'',
        isShow: false
      }
    },
    methods: {
      show(message='默认文字', duration = 1500) {
        this.isShow = true;
        this.message = message
        // console.log('我是show方法');
        setTimeout(() => {
          this.isShow = false;
          this.message = ''
        }, duration);
      }
    }
  }
</script>
<style scoped>
  .toast {
    position: fixed;
    top: 50%;
    left: 50%;
    transform: translate(-50%,-50%);
    padding: 8px 10px;
    color: #FFF;
    z-index: 999;
    background-color: rgba(0, 0, 0, 0.6);

    transition: all .3s ease-in-out;
  }
</style>